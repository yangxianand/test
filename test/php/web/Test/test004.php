<?php
$n=5;
for($i=1;$i<=$n;$i++){
	for($j=1;$j<=$n-$i;$j++){
		echo'&nbsp;';
	}
	for($k=1;$k<=(2*$i)-1;$k++){
		echo'*';
	}
	echo'<br>';
}

for($i=$n;$i>=1;$i--){
	for($j=1;$j<=$n-$i;$j++){
		echo'&nbsp;';
	}
	for($k=1;$k<=(2*$i)-1;$k++){
		echo'*';
	}
	echo'<br>';
}
for($i=1;$i<=$n;$i++){
	for($j=1;$j<=$i-1;$j++){
		echo'&nbsp;';
	}
	for($k=1;$k<=2*($n-$i)+1;$k++){
		echo'*';
	}
	echo'<br>';
}
?>